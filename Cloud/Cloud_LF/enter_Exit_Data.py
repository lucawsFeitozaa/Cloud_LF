numero_1 = int(input('Digite primeiro numero: '))
numero_2 = int(input('Digite segundo numero: '))

print(
    numero_1 + numero_2,  # Soma
    numero_1 - numero_2,  # Subtração
    numero_1 * numero_2,  # Multiplicação
    numero_1 / numero_2,  # Divisão
    numero_1 % numero_2,  # Resto da divisão
)